import { mkdtemp, mkdir, readFile, rm, writeFile } from "node:fs/promises";
import path from "node:path";
import { tmpdir } from "node:os";
import { fileURLToPath } from "node:url";
import { afterEach, describe, expect, it } from "vitest";
import { generateAuthFunctions } from "../src/cli/generate";

const tempDirs: string[] = [];
const packageDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const convexZenEntry = path.join(packageDir, "src", "client", "index.ts");
const convexZenComponentEntry = path.join(
  packageDir,
  "src",
  "component",
  "index.ts"
);
const systemAdminPluginEntry = path.join(
  packageDir,
  "src",
  "plugins",
  "system-admin",
  "index.ts"
);
const organizationPluginEntry = path.join(
  packageDir,
  "src",
  "plugins",
  "organization",
  "index.ts"
);
const examplePluginPackage = "convex-zen-example";
const GENERATED_MARKER = "// @generated by convex-zen generate. DO NOT EDIT.";

async function createTempWorkspace(): Promise<string> {
  const cwd = await mkdtemp(path.join(tmpdir(), "convex-zen-generate-"));
  tempDirs.push(cwd);
  await mkdir(path.join(cwd, "convex"), { recursive: true });
  return cwd;
}

async function writeZenConfigFile(cwd: string, source: string): Promise<void> {
  await writeFile(path.join(cwd, "convex", "zen.config.ts"), source, "utf8");
}

async function writeLocalPluginFiles(
  cwd: string,
  options: {
    pluginId: string;
    exportName: string;
    functionName?: string;
    schemaTableName?: string;
  }
): Promise<void> {
  const functionName = options.functionName ?? "getMessage";
  const schemaTableName = options.schemaTableName ?? `${options.pluginId}__messages`;
  const pluginDir = path.join(cwd, "convex", "plugins", options.pluginId);
  await mkdir(pluginDir, { recursive: true });

  await writeFile(
    path.join(pluginDir, "schema.ts"),
    `
import { defineTable } from "convex/server";
import { v } from "convex/values";
import { definePluginSchema } from ${JSON.stringify(convexZenEntry)};

export const schema = definePluginSchema({
  tables: {
    ${JSON.stringify(schemaTableName)}: defineTable({
      value: v.string(),
      createdAt: v.number(),
    }),
  },
});
`,
    "utf8"
  );

  await writeFile(
    path.join(pluginDir, "gateway.ts"),
    `
import { v } from "convex/values";
import { pluginQuery } from ${JSON.stringify(convexZenComponentEntry)};

export const ${functionName} = pluginQuery({
  auth: "public",
  args: {
    value: v.optional(v.string()),
  },
  handler: async (_ctx, args) => args.value ?? "hello",
});
`,
    "utf8"
  );

  await writeFile(
    path.join(pluginDir, "index.ts"),
    `
import { definePlugin } from ${JSON.stringify(convexZenEntry)};
import * as gateway from "./gateway";
import { schema } from "./schema";

export const ${options.exportName} = definePlugin({
  id: ${JSON.stringify(options.pluginId)},
  schema,
  gateway,
});
`,
    "utf8"
  );
}

async function writeInstalledPluginPackage(
  cwd: string,
  options: {
    packageName: string;
    factoryImportPath?: string;
    exportName: string;
    pluginId: string;
    exports?: Record<string, { import?: string; types?: string }>;
    main?: string;
  }
): Promise<void> {
  const importPath = options.factoryImportPath ?? ".";
  const baseDir =
    importPath === "."
      ? path.join(cwd, "node_modules", options.packageName, "dist")
      : path.join(
          cwd,
          "node_modules",
          options.packageName,
          importPath.replace(/^\.\//, "")
        );

  await mkdir(baseDir, { recursive: true });
  await writeFile(
    path.join(cwd, "node_modules", options.packageName, "package.json"),
    JSON.stringify(
      {
        name: options.packageName,
        version: "1.0.0",
        type: "module",
        ...(options.main ? { main: options.main } : {}),
        ...(options.exports ? { exports: options.exports } : {}),
      },
      null,
      2
    ),
    "utf8"
  );

  await writeFile(
    path.join(baseDir, "index.js"),
    `
const PLUGIN_FUNCTION_METADATA_KEY = "__convexZenPluginFunction";

const gateway = {
  getMessage: {
    [PLUGIN_FUNCTION_METADATA_KEY]: {
      kind: "query",
      auth: "public",
      args: {},
    },
  },
};

export function ${options.exportName}(options) {
  return {
    id: ${JSON.stringify(options.pluginId)},
    definition: ${options.exportName}.definition,
    options,
  };
}

${options.exportName}.definition = {
  id: ${JSON.stringify(options.pluginId)},
  gateway,
};
`,
    "utf8"
  );
}

afterEach(async () => {
  await Promise.all(
    tempDirs.splice(0).map(async (dir) => {
      await rm(dir, { recursive: true, force: true });
    })
  );
});

describe("generateAuthFunctions", () => {
  it("generates a flat build-time auth surface for built-in plugins", async () => {
    const cwd = await createTempWorkspace();
    await writeZenConfigFile(
      cwd,
      `
import { defineConvexZen } from ${JSON.stringify(convexZenEntry)};
import { systemAdminPlugin } from ${JSON.stringify(systemAdminPluginEntry)};

export default defineConvexZen({
  plugins: [systemAdminPlugin()],
});
`
    );

    const result = await generateAuthFunctions({
      cwd,
      check: false,
      verbose: false,
    });

    expect(result.created).toContain(
      path.join("convex", "zen", "component", "schema.ts")
    );
    expect(result.created).toContain(
      path.join("convex", "zen", "component", "_runtime.ts")
    );
    expect(result.created).toContain(
      path.join("convex", "zen", "component", "plugins", "systemAdmin", "gateway.ts")
    );
    expect(result.created).not.toContain(
      path.join("convex", "zen", "component", "gateway.ts")
    );
    expect(result.created).toContain(
      path.join("convex", "zen", "plugin", "systemAdmin.ts")
    );

    const componentConfigSource = await readFile(
      path.join(cwd, "convex", "zen", "component", "convex.config.ts"),
      "utf8"
    );
    expect(componentConfigSource).toContain('defineComponent("zenComponent")');
    expect(componentConfigSource).not.toContain(".use(");

    const componentSchemaSource = await readFile(
      path.join(cwd, "convex", "zen", "component", "schema.ts"),
      "utf8"
    );
    expect(componentSchemaSource).toContain("coreSchemaTables");
    expect(componentSchemaSource).toContain(
      "systemAdminPluginFactory.definition.schema?.tables"
    );

    const runtimeSource = await readFile(
      path.join(cwd, "convex", "zen", "component", "_runtime.ts"),
      "utf8"
    );
    expect(runtimeSource).toContain('runtimeKind: "component"');
    expect(runtimeSource).not.toContain("plugins/systemAdmin/gateway:listUsers");
    expect(runtimeSource).toContain(
      'componentFn<"mutation">("core/users:remove")'
    );

    const appPluginSource = await readFile(
      path.join(cwd, "convex", "zen", "plugin", "systemAdmin.ts"),
      "utf8"
    );
    expect(appPluginSource).toContain(
      "const pluginGateway = systemAdminPluginFactory.definition.gateway;"
    );
    expect(appPluginSource).toContain(
      "components.zenComponent.plugins.systemAdmin.gateway.listUsers"
    );
    expect(appPluginSource).not.toContain('import { auth } from "../_generated/auth";');

    const componentPluginSource = await readFile(
      path.join(
        cwd,
        "convex",
        "zen",
        "component",
        "plugins",
        "systemAdmin",
        "gateway.ts"
      ),
      "utf8"
    );
    expect(componentPluginSource).toContain('import { auth } from "../../_runtime";');
    expect(componentPluginSource).toContain(
      'runPluginRuntimeFunction(ctx, "listUsers", args)'
    );
    expect(componentPluginSource).toContain(
      'type PluginRuntimeReturn<TFunctionName extends keyof PluginRuntime & string>'
    );
    expect(componentPluginSource).toContain(
      'args: Record<string, unknown>'
    );

    const metaSource = await readFile(
      path.join(cwd, "convex", "zen", "_generated", "meta.ts"),
      "utf8"
    );
    expect(metaSource).toContain('"systemAdmin"');
    expect(metaSource).toContain('"listUsers": "query"');
  });

  it("cleans up legacy child-component files when plugins are disabled", async () => {
    const cwd = await createTempWorkspace();
    await writeZenConfigFile(
      cwd,
      `
import { defineConvexZen } from ${JSON.stringify(convexZenEntry)};

export default defineConvexZen({
  plugins: [],
});
`
    );

    await mkdir(path.join(cwd, "convex", "zen", "plugin"), { recursive: true });
    await mkdir(
      path.join(cwd, "convex", "zen", "component", "plugins", "systemAdmin"),
      { recursive: true }
    );
    await mkdir(
      path.join(cwd, "convex", "zen", "component", "systemAdmin"),
      { recursive: true }
    );
    await mkdir(path.join(cwd, "convex", "zen", "component"), {
      recursive: true,
    });
    await writeFile(
      path.join(cwd, "convex", "zen", "plugin", "systemAdmin.ts"),
      `${GENERATED_MARKER}\nexport const stale = true;\n`,
      "utf8"
    );
    await writeFile(
      path.join(cwd, "convex", "zen", "component", "plugins", "systemAdmin", "gateway.ts"),
      `${GENERATED_MARKER}\nexport const stale = true;\n`,
      "utf8"
    );
    await writeFile(
      path.join(cwd, "convex", "zen", "component", "systemAdmin", "gateway.ts"),
      `${GENERATED_MARKER}\nexport const stale = true;\n`,
      "utf8"
    );
    await writeFile(
      path.join(cwd, "convex", "zen", "component", "gateway.ts"),
      `${GENERATED_MARKER}\nexport const stale = true;\n`,
      "utf8"
    );

    const result = await generateAuthFunctions({
      cwd,
      check: false,
      verbose: false,
    });

    expect(result.deleted).toContain(
      path.join("convex", "zen", "plugin", "systemAdmin.ts")
    );
    expect(result.deleted).toContain(
      path.join("convex", "zen", "component", "plugins", "systemAdmin", "gateway.ts")
    );
    expect(result.deleted).toContain(
      path.join("convex", "zen", "component", "systemAdmin", "gateway.ts")
    );
    expect(result.deleted).toContain(
      path.join("convex", "zen", "component", "gateway.ts")
    );

    const componentConfigSource = await readFile(
      path.join(cwd, "convex", "zen", "component", "convex.config.ts"),
      "utf8"
    );
    expect(componentConfigSource).not.toContain(".use(");

    const metaSource = await readFile(
      path.join(cwd, "convex", "zen", "_generated", "meta.ts"),
      "utf8"
    );
    expect(metaSource).toContain('"plugin": {}');
  });

  it("detects shorthand providers config and omits unsafe public wrappers", async () => {
    const cwd = await createTempWorkspace();
    await writeZenConfigFile(
      cwd,
      `
import { defineConvexZen, googleProvider } from ${JSON.stringify(convexZenEntry)};

const providers = [
  googleProvider({
    clientId: "google-client-id",
    clientSecret: "google-client-secret",
  }),
];

export default defineConvexZen({
  providers,
});
`
    );

    await generateAuthFunctions({
      cwd,
      check: false,
      verbose: false,
    });

    const metaSource = await readFile(
      path.join(cwd, "convex", "zen", "_generated", "meta.ts"),
      "utf8"
    );
    expect(metaSource).toContain('"getOAuthUrl": "mutation"');
    expect(metaSource).toContain('"handleOAuthCallback": "action"');

    const runtimeSource = await readFile(
      path.join(cwd, "convex", "zen", "component", "_runtime.ts"),
      "utf8"
    );
    expect(runtimeSource).toContain(
      'componentFn<"mutation">("core/gateway:getAuthorizationUrl")'
    );
    expect(runtimeSource).toContain(
      'componentFn<"action">("core/gateway:handleCallback")'
    );

    const coreSource = await readFile(
      path.join(cwd, "convex", "zen", "core.ts"),
      "utf8"
    );
    expect(coreSource).not.toContain("export const getUserById = query({");
    expect(coreSource).not.toContain(
      "export const invalidateAllSessions = mutation({"
    );
  });

  it("ignores commented-out plugin factories and only emits active plugin wrappers", async () => {
    const cwd = await createTempWorkspace();
    await writeZenConfigFile(
      cwd,
      `
import { defineConvexZen } from ${JSON.stringify(convexZenEntry)};
import { systemAdminPlugin } from ${JSON.stringify(systemAdminPluginEntry)};
import { organizationPlugin } from ${JSON.stringify(organizationPluginEntry)};

export default defineConvexZen({
  plugins: [
    // systemAdminPlugin(),
    organizationPlugin(),
  ],
});
`
    );

    await generateAuthFunctions({
      cwd,
      check: false,
      verbose: false,
    });

    const metaSource = await readFile(
      path.join(cwd, "convex", "zen", "_generated", "meta.ts"),
      "utf8"
    );
    expect(metaSource).toContain('"organization"');
    expect(metaSource).not.toContain('"systemAdmin"');

    await expect(
      readFile(path.join(cwd, "convex", "zen", "plugin", "systemAdmin.ts"), "utf8")
    ).rejects.toThrow();
    const organizationPluginSource = await readFile(
      path.join(cwd, "convex", "zen", "plugin", "organization.ts"),
      "utf8"
    );
    expect(organizationPluginSource).toContain(
      "organizationPluginFactory.definition.gateway"
    );
  });

  it("loads workspace plugins from their main entry and merges plugin schema", async () => {
    const cwd = await createTempWorkspace();
    await writeLocalPluginFiles(cwd, {
      pluginId: "custom",
      exportName: "customPlugin",
    });
    await writeZenConfigFile(
      cwd,
      `
import { defineConvexZen } from ${JSON.stringify(convexZenEntry)};
import { customPlugin } from "./plugins/custom";

export default defineConvexZen({
  plugins: [customPlugin()],
});
`
    );

    await generateAuthFunctions({
      cwd,
      check: false,
      verbose: false,
    });

    const componentSchemaSource = await readFile(
      path.join(cwd, "convex", "zen", "component", "schema.ts"),
      "utf8"
    );
    expect(componentSchemaSource).toContain(
      "customPluginFactory.definition.schema?.tables"
    );

    const appPluginSource = await readFile(
      path.join(cwd, "convex", "zen", "plugin", "custom.ts"),
      "utf8"
    );
    expect(appPluginSource).toContain(
      'import { customPlugin as customPluginFactory } from "../../plugins/custom/index";'
    );
    expect(appPluginSource).toContain(
      "const pluginGateway = customPluginFactory.definition.gateway;"
    );
    expect(appPluginSource).toContain(
      "components.zenComponent.plugins.custom.gateway.getMessage"
    );

    const componentPluginSource = await readFile(
      path.join(cwd, "convex", "zen", "component", "plugins", "custom", "gateway.ts"),
      "utf8"
    );
    expect(componentPluginSource).toContain(
      'runPluginRuntimeFunction(ctx, "getMessage", args)'
    );
  });

  it("loads external plugin packages from the factory main entry", async () => {
    const cwd = await createTempWorkspace();
    await writeZenConfigFile(
      cwd,
      `
import { defineConvexZen } from ${JSON.stringify(convexZenEntry)};
import { examplePlugin } from ${JSON.stringify(examplePluginPackage)};

export default defineConvexZen({
  plugins: [examplePlugin()],
});
`
    );

    await generateAuthFunctions({
      cwd,
      check: false,
      verbose: false,
    });

    const pluginSource = await readFile(
      path.join(cwd, "convex", "zen", "plugin", "example.ts"),
      "utf8"
    );
    expect(pluginSource).toContain(
      'import { examplePlugin as examplePluginFactory } from "convex-zen-example";'
    );
    expect(pluginSource).toContain(
      "const pluginGateway = examplePluginFactory.definition.gateway;"
    );
    expect(pluginSource).toContain(
      "components.zenComponent.plugins.example.gateway.listLogs"
    );
  });

  it("supports installed packages that only expose the factory root", async () => {
    const cwd = await createTempWorkspace();
    await writeInstalledPluginPackage(cwd, {
      packageName: "acme-installed-plugin",
      exportName: "installedPlugin",
      pluginId: "installed",
      exports: {
        ".": {
          import: "./dist/index.js",
        },
      },
    });
    await writeZenConfigFile(
      cwd,
      `
import { defineConvexZen } from ${JSON.stringify(convexZenEntry)};
import { installedPlugin } from "acme-installed-plugin";

export default defineConvexZen({
  plugins: [installedPlugin()],
});
`
    );

    await generateAuthFunctions({
      cwd,
      check: false,
      verbose: false,
    });

    const pluginSource = await readFile(
      path.join(cwd, "convex", "zen", "plugin", "installed.ts"),
      "utf8"
    );
    expect(pluginSource).toContain(
      'import { installedPlugin as installedPluginFactory } from "acme-installed-plugin";'
    );
    expect(pluginSource).toContain(
      "const pluginGateway = installedPluginFactory.definition.gateway;"
    );
    expect(pluginSource).toContain(
      "components.zenComponent.plugins.installed.gateway.getMessage"
    );
  });

  it("falls back to main when package exports only provide types for the root", async () => {
    const cwd = await createTempWorkspace();
    await writeInstalledPluginPackage(cwd, {
      packageName: "acme-types-export-plugin",
      exportName: "typesExportPlugin",
      pluginId: "typesExport",
      main: "./dist/index.js",
      exports: {
        ".": {
          types: "./dist/index.d.ts",
        },
      },
    });
    await writeFile(
      path.join(
        cwd,
        "node_modules",
        "acme-types-export-plugin",
        "dist",
        "index.d.ts"
      ),
      `export declare function typesExportPlugin(options?: unknown): unknown;\n`,
      "utf8"
    );
    await writeZenConfigFile(
      cwd,
      `
import { defineConvexZen } from ${JSON.stringify(convexZenEntry)};
import { typesExportPlugin } from "acme-types-export-plugin";

export default defineConvexZen({
  plugins: [typesExportPlugin()],
});
`
    );

    await generateAuthFunctions({
      cwd,
      check: false,
      verbose: false,
    });

    const pluginSource = await readFile(
      path.join(cwd, "convex", "zen", "plugin", "typesExport.ts"),
      "utf8"
    );
    expect(pluginSource).toContain(
      'import { typesExportPlugin as typesExportPluginFactory } from "acme-types-export-plugin";'
    );
    expect(pluginSource).toContain(
      "typesExportPluginFactory.definition.gateway"
    );
  });

  it("supports plugins imported from subpath index files", async () => {
    const cwd = await createTempWorkspace();
    await writeInstalledPluginPackage(cwd, {
      packageName: "acme-subpath-plugin",
      factoryImportPath: "factory",
      exportName: "subpathPlugin",
      pluginId: "subpath",
    });
    await writeZenConfigFile(
      cwd,
      `
import { defineConvexZen } from ${JSON.stringify(convexZenEntry)};
import { subpathPlugin } from "acme-subpath-plugin/factory";

export default defineConvexZen({
  plugins: [subpathPlugin()],
});
`
    );

    await generateAuthFunctions({
      cwd,
      check: false,
      verbose: false,
    });

    const pluginSource = await readFile(
      path.join(cwd, "convex", "zen", "plugin", "subpath.ts"),
      "utf8"
    );
    expect(pluginSource).toContain(
      'import { subpathPlugin as subpathPluginFactory } from "acme-subpath-plugin/factory";'
    );
    expect(pluginSource).toContain(
      "subpathPluginFactory.definition.gateway"
    );
  });

  it("rejects plugin tables that are not prefixed with the plugin id", async () => {
    const cwd = await createTempWorkspace();
    await writeLocalPluginFiles(cwd, {
      pluginId: "custom",
      exportName: "customPlugin",
      schemaTableName: "messages",
    });
    await writeZenConfigFile(
      cwd,
      `
import { defineConvexZen } from ${JSON.stringify(convexZenEntry)};
import { customPlugin } from "./plugins/custom";

export default defineConvexZen({
  plugins: [customPlugin()],
});
`
    );

    await expect(
      generateAuthFunctions({
        cwd,
        check: false,
        verbose: false,
      })
    ).rejects.toThrow(
      'Invalid table "messages" for plugin "custom". Plugin tables must be prefixed with "custom__".'
    );
  });

  it("rejects duplicate kebab-case plugin route keys", async () => {
    const cwd = await createTempWorkspace();
    await writeLocalPluginFiles(cwd, {
      pluginId: "fooBar",
      exportName: "fooBarPlugin",
      functionName: "listUsers",
    });
    await writeLocalPluginFiles(cwd, {
      pluginId: "foo_bar",
      exportName: "fooBarUnderscorePlugin",
      functionName: "listUsers",
    });
    await writeZenConfigFile(
      cwd,
      `
import { defineConvexZen } from ${JSON.stringify(convexZenEntry)};
import { fooBarPlugin } from "./plugins/fooBar";
import { fooBarUnderscorePlugin } from "./plugins/foo_bar";

export default defineConvexZen({
  plugins: [fooBarPlugin(), fooBarUnderscorePlugin()],
});
`
    );

    await expect(
      generateAuthFunctions({
        cwd,
        check: false,
        verbose: false,
      })
    ).rejects.toThrow(
      'Duplicate generated plugin route "foo-bar/list-users". Rename one of the conflicting plugin functions.'
    );
  });
});
