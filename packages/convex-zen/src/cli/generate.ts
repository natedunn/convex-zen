function hasOAuthProviders(authSource: string): boolean {
  // Strip block and line comments so that commented-out providers are not detected.
  // Note: this regex-based stripping does not account for comment-like sequences
  // inside string literals, which is an acceptable limitation for zen.config.ts files.
  const stripped = authSource
    .replace(/\/\*[\s\S]*?\*\//g, "")
    .replace(/\/\/[^\n]*/g, "");

  // Locate the opening parenthesis of the defineConvexZen(...) call.
  const callMatch = stripped.match(/defineConvexZen\s*\(/);
  if (!callMatch || callMatch.index === undefined) return false;

  // Walk forward tracking parenthesis depth to extract only the call's arguments,
  // avoiding false positives from any `providers:` that appears outside this call.
  let depth = 1;
  let i = callMatch.index + callMatch[0].length;
  while (i < stripped.length && depth > 0) {
    const ch = stripped[i];
    if (ch === "(") depth++;
    else if (ch === ")") depth--;
    i++;
  }

  const callContent = stripped.slice(callMatch.index + callMatch[0].length, i - 1);
  return /\bproviders\s*:|\bproviders\s*(?=,|\})/.test(callContent);
}

type ImportedBinding = {
  importedName: string;
  moduleSpecifier: string;
};

function parseNamedImports(authSource: string): Map<string, ImportedBinding> {
  const bindings = new Map<string, ImportedBinding>();
  const importPattern =
    /import\s*\{([^}]+)\}\s*from\s*["']([^"']+)["'];?/g;
  let match = importPattern.exec(authSource);
  while (match) {
    const specifiers = match[1]?.split(",") ?? [];
    const moduleSpecifier = match[2];
    for (const specifier of specifiers) {
      const trimmed = specifier.trim();
      if (!trimmed || !moduleSpecifier) {
        continue;
      }
      const [importedName, localName] = trimmed.split(/\s+as\s+/);
      const resolvedImportedName = importedName?.trim();
      const resolvedLocalName = (localName ?? importedName)?.trim();
      if (!resolvedImportedName || !resolvedLocalName) {
        continue;
      }
      bindings.set(resolvedLocalName, {
        importedName: resolvedImportedName,
        moduleSpecifier,
      });
    }
    match = importPattern.exec(authSource);
  }
  return bindings;
}

function detectConfiguredPluginFactories(authSource: string): string[] {
  const pluginsMatch = authSource.match(/plugins\s*:\s*\[([\s\S]*?)\]/m);
  if (!pluginsMatch?.[1]) {
    return [];
  }
  const uncommentedPluginsSource = pluginsMatch[1]
    .replace(/\/\*[\s\S]*?\*\//g, "")
    .replace(/^\s*\/\/.*$/gm, "");
  const factoryIds: string[] = [];
  const callPattern = /([A-Za-z0-9_]+)\s*\(/g;
  let match = callPattern.exec(uncommentedPluginsSource);
  while (match) {
    if (match[1]) {
      factoryIds.push(match[1]);
    }
    match = callPattern.exec(uncommentedPluginsSource);
  }
  return factoryIds;
}

async function importPluginDefinition(
  authSource: ResolvedAuthSource,
  binding: ImportedBinding
): Promise<PluginDefinition> {
  const moduleSpecifier = await resolveImportSpecifier(authSource, binding.moduleSpecifier);
  const moduleNamespace = (await import(moduleSpecifier)) as Record<string, unknown>;
  const pluginFactory = moduleNamespace[binding.importedName] as {
    definition?: PluginDefinition;
  };
  if (!pluginFactory?.definition) {
    throw new Error(
      `Plugin factory "${binding.importedName}" from "${binding.moduleSpecifier}" does not expose static definition metadata.`
    );
  }
  return pluginFactory.definition;
}

function toKebabCase(value: string): string {
  return value
    .replace(/([a-z0-9])([A-Z])/g, "$1-$2")
    .replace(/[_\s]+/g, "-")
    .replace(/-+/g, "-")
    .toLowerCase();
}

function toGeneratedImportPath(
  sourceFileAbsolutePath: string,
  targetFileAbsolutePath: string
): string {
  const relativeImport = path.relative(
    path.dirname(sourceFileAbsolutePath),
    targetFileAbsolutePath
  );
  const normalized = relativeImport.replace(/\\/g, "/").replace(/\.ts$/, "");
  return normalized.startsWith(".") ? normalized : `./${normalized}`;
}

async function resolvePluginEntrySourcePath(
  authSource: ResolvedAuthSource,
  pluginModuleSpecifier: string,
  pluginDefinition: PluginDefinition
): Promise<string> {
  if (!pluginModuleSpecifier.startsWith(".") && !path.isAbsolute(pluginModuleSpecifier)) {
    return pluginModuleSpecifier;
  }
  const pluginModulePath = await resolvePluginModulePath(authSource, pluginModuleSpecifier);
  if (!pluginModulePath) {
    throw new Error(
      `Could not resolve plugin module path for "${pluginDefinition.id}" from "${pluginModuleSpecifier}".`
    );
  }
  return pluginModulePath;
}

async function resolvePluginModulePath(
  authSource: ResolvedAuthSource,
  pluginModuleSpecifier: string
): Promise<string | null> {
  if (path.isAbsolute(pluginModuleSpecifier)) {
    return pluginModuleSpecifier;
  }
  if (!pluginModuleSpecifier.startsWith(".")) {
    if (pluginModuleSpecifier === "convex-zen") {
      return null;
    }
    const moduleSpecifier = await resolveImportSpecifier(authSource, pluginModuleSpecifier);
    return moduleSpecifier.startsWith("file:")
      ? fileURLToPath(moduleSpecifier)
      : null;
  }
  const resolved = path.resolve(path.dirname(authSource.absolutePath), pluginModuleSpecifier);
  if (await fileExists(resolved)) {
    const resolvedStats = await stat(resolved);
    if (resolvedStats.isFile()) {
      return resolved;
    }
  }
  const indexPath = path.join(resolved, "index.ts");
  if (await fileExists(indexPath)) {
    return indexPath;
  }
  const directTsPath = `${resolved}.ts`;
  if (await fileExists(directTsPath)) {
    return directTsPath;
  }
  return resolved;
}

async function resolveImportSpecifier(
  authSource: ResolvedAuthSource,
  moduleSpecifier: string
): Promise<string> {
  if (moduleSpecifier.startsWith(".")) {
    return pathToFileURL(
      path.resolve(path.dirname(authSource.absolutePath), moduleSpecifier)
    ).href;
  }
  if (moduleSpecifier === "convex-zen") {
    const workspaceRoot = await findWorkspaceRoot(path.dirname(authSource.absolutePath));
    if (workspaceRoot) {
      return pathToFileURL(
        path.join(workspaceRoot, "packages", "convex-zen", "src", "client", "index.ts")
      ).href;
    }
  }
  if (moduleSpecifier.startsWith("convex-zen/")) {
    const workspaceRoot = await findWorkspaceRoot(path.dirname(authSource.absolutePath));
    if (workspaceRoot) {
      const subpath = moduleSpecifier.slice("convex-zen/".length);
      const mapped = subpath === "component"
        ? path.join(workspaceRoot, "packages", "convex-zen", "src", "component", "index.ts")
        : subpath === "convex.config"
          ? path.join(workspaceRoot, "packages", "convex-zen", "src", "component", "convex.config.ts")
            : subpath === "core/convex.config"
              ? path.join(workspaceRoot, "packages", "convex-zen", "src", "component", "core", "convex.config.ts")
              : subpath === "component/core-schema-definition"
                ? path.join(
                    workspaceRoot,
                    "packages",
                    "convex-zen",
                    "src",
                    "component",
                    "core",
                    "schemaDefinition.ts"
                  )
            : subpath.startsWith("plugins/")
              ? path.join(
                  workspaceRoot,
                  "packages",
                  "convex-zen",
                  "src",
                  "plugins",
                  subpath.replace(/^plugins\//, ""),
                  "index.ts"
                  )
              : path.join(workspaceRoot, "packages", "convex-zen", "src", "client", `${subpath}.ts`);
      if (await fileExists(mapped)) {
        return pathToFileURL(mapped).href;
      }
    }
  }
  if (!moduleSpecifier.startsWith(".")) {
    const workspaceImport = await resolveWorkspacePackageImport(
      authSource,
      moduleSpecifier
    );
    if (workspaceImport) {
      return workspaceImport;
    }
    const installedImport = await resolveInstalledPackageImport(
      authSource,
      moduleSpecifier
    );
    if (installedImport) {
      return installedImport;
    }
    const resolved = nodeRequire.resolve(moduleSpecifier, {
      paths: [path.dirname(authSource.absolutePath)],
    });
    return pathToFileURL(resolved).href;
  }
  return moduleSpecifier;
}

async function findWorkspaceRoot(startDir: string): Promise<string | null> {
  let currentDir = startDir;
  while (true) {
    const candidate = path.join(currentDir, "packages", "convex-zen", "package.json");
    if (await fileExists(candidate)) {
      return currentDir;
    }
    const parentDir = path.dirname(currentDir);
    if (parentDir === currentDir) {
      return null;
    }
    currentDir = parentDir;
  }
}

async function loadPluginDefinitions(
  authSource: ResolvedAuthSource,
  authSourceContents: string
): Promise<LoadedPluginDefinition[]> {
  const imports = parseNamedImports(authSourceContents);
  const factoryIds = detectConfiguredPluginFactories(authSourceContents);
  const definitions: LoadedPluginDefinition[] = [];
  for (const factoryId of factoryIds) {
    const binding = imports.get(factoryId);
    if (!binding) {
      throw new Error(
        `Could not resolve plugin factory "${factoryId}" from ${authSource.relativePath}.`
      );
    }
    const definition = await importPluginDefinition(authSource, binding);
    const gatewayFunctions = collectPluginGatewayMetadata(definition.gateway);
    definitions.push({
      definition,
      entrySourcePath: await resolvePluginEntrySourcePath(
        authSource,
        binding.moduleSpecifier,
        definition
      ),
      factoryExportName: binding.importedName,
      factoryImportName: sanitizeIdentifier(`${definition.id}PluginFactory`),
      gatewayFunctions,
    });
  }
  return definitions;
}

function sanitizeIdentifier(value: string): string {
  const sanitized = value.replace(/[^A-Za-z0-9_]/g, "_");
  return /^[A-Za-z_]/.test(sanitized) ? sanitized : `plugin_${sanitized}`;
}

