function parseFunctionKinds(source: string): Record<string, AuthFunctionKind> {
  const result: Record<string, AuthFunctionKind> = {};
  const pattern = /export\s+const\s+([A-Za-z0-9_]+)\s*=\s*(query|mutation|action)\s*\(/g;
  let match = pattern.exec(source);
  while (match) {
    const functionName = match[1];
    const functionKind = match[2] as AuthFunctionKind;
    if (functionName) {
      result[functionName] = functionKind;
    }
    match = pattern.exec(source);
  }
  return result;
}

function parseExportedFunctionNames(source: string): Set<string> {
  const names = new Set<string>();
  const pattern = /export\s+const\s+([A-Za-z0-9_]+)\s*=/g;
  let match = pattern.exec(source);
  while (match) {
    if (match[1]) {
      names.add(match[1]);
    }
    match = pattern.exec(source);
  }
  return names;
}

function assertNoReservedCoreFunctionNames(source: string): void {
  const exportedNames = parseExportedFunctionNames(source);
  const conflicts: string[] = [];
  for (const reservedName of RESERVED_CORE_FUNCTION_NAMES) {
    if (exportedNames.has(reservedName)) {
      conflicts.push(reservedName);
    }
  }
  if (conflicts.length === 0) {
    return;
  }
  throw new Error(
    `Generated core wrappers include reserved auth-client names: ${conflicts.join(", ")}. ` +
      "Rename core wrappers to non-reserved names."
  );
}

function assertNoPluginConflicts(pluginDefinitions: LoadedPluginDefinition[]): void {
  const pluginIds = new Set<string>();
  const tableNames = new Set<string>(CORE_TABLE_NAMES);
  const routeKeys = new Set<string>();

  for (const plugin of pluginDefinitions) {
    const pluginId = plugin.definition.id;
    if (pluginIds.has(pluginId)) {
      throw new Error(`Duplicate auth plugin id "${pluginId}".`);
    }
    if (RESERVED_PLUGIN_IDS.has(pluginId)) {
      throw new Error(`Invalid auth plugin id "${pluginId}". This id is reserved.`);
    }
    pluginIds.add(pluginId);

    for (const functionName of Object.keys(plugin.gatewayFunctions)) {
      const routeKey = `${toKebabCase(pluginId)}/${toKebabCase(functionName)}`;
      if (routeKeys.has(routeKey)) {
        throw new Error(
          `Duplicate generated plugin route "${routeKey}". Rename one of the conflicting plugin functions.`
        );
      }
      routeKeys.add(routeKey);
    }

    const tables = plugin.definition.schema?.tables ?? {};
    for (const tableName of Object.keys(tables)) {
      if (!tableName.startsWith(`${pluginId}__`)) {
        throw new Error(
          `Invalid table "${tableName}" for plugin "${pluginId}". Plugin tables must be prefixed with "${pluginId}__".`
        );
      }
      if (tableNames.has(tableName)) {
        throw new Error(`Duplicate merged schema table "${tableName}".`);
      }
      tableNames.add(tableName);
    }
  }
}

